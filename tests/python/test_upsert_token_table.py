def test_first_run_uses_summary_text() -> None:
    """On first run, the posted body is built from summary_text, not fetched."""
    provider = _FakeProvider()
    _run_upsert(provider, summary_text="## My Summary", is_incremental=False)
    assert provider.posted_bodies
    posted = provider.posted_bodies[-1]
    assert "## My Summary" in posted
    assert "<details>" in posted
    # get_summary_body() must NOT have been called on the first-run path
    # (provider.posted_bodies would be the only side-effect; no stored_body
    # access is observable, but we verify no old content leaks through)
    assert "old table" not in posted


def test_first_run_falls_back_to_ai_review_header_when_no_summary() -> None:
    """Empty summary_text on first run produces the '## AI Review' fallback."""
    provider = _FakeProvider()
    _run_upsert(provider, summary_text="", is_incremental=False)
    assert provider.posted_bodies
    posted = provider.posted_bodies[-1]
    assert posted.startswith("## AI Review")
